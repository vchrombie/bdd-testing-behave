# Simple Calculator
#

class Calculator(object):
    def __init__(self, caching=True):
        """ init """
        pass

    def add(self, xxx, yyy):
        """ addition """
        return xxx + yyy

    def sub(self, xxx, yyy):
        """ substraction """
        return xxx - yyy

    def mult(self, xxx, yyy):
        """ multiply """
        return xxx * yyy

    def div(self, xxx, yyy):
        """ divide """
        return xxx / yyy
